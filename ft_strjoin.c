#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	i;
	size_t	j;
	size_t	l;
	char	*p;

	if (!s1 || !s2)
		return (NULL);
	l = ft_strlen((char *)s1) + ft_strlen((char *)s2);
	i = 0;
	p = (char *) malloc ((l + 1) * sizeof(char));
	if (!p)
		return (NULL);
	while (s1[i] != '\0')
	{
		p[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j] != '\0')
	{
		p[i + j] = s2[j];
		j++;
	}
	p[i + j] = '\0';
	return (p);
}
